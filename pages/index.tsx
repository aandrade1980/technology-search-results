import type { NextPage } from 'next';
import Head from 'next/head';
import { useState } from 'react';

import { Container, Row, Input, Spacer, Loading } from '@nextui-org/react';

import { Button } from '@nextui-org/react';

const Home: NextPage = () => {
  const [searchText, setSearchText] = useState('');
  const [searchResults, setSearchResults] = useState([]);
  const [isSearching, setIsSearching] = useState(false);

  const handleSearch = async (e: React.FormEvent<HTMLFormElement>) => {
    e.preventDefault();

    if (!searchText) {
      return;
    }

    setIsSearching(true);

    const results = await fetch(`/api/getResults?searchText=${searchText}`);
    const data = await results.json();

    setSearchResults(data);
    setIsSearching(false);
  };

  console.log(searchResults);

  return (
    <div>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Container as="main">
        <Row justify="center" align="center">
          <h1>Welcome to Search Technology</h1>
        </Row>
        <Row justify="center" align="center">
          <form onSubmit={handleSearch}>
            <Container display="flex">
              <Input
                placeholder="Search for something"
                clearable
                bordered
                color="primary"
                type="text"
                name="search"
                id="search"
                value={searchText}
                onChange={e => setSearchText(e.target.value)}
              />
              <Spacer x={1} />
              <Button shadow type="submit">
                {isSearching ? (
                  <Loading color="currentColor" type="points" size="sm" />
                ) : (
                  'Search'
                )}
              </Button>
            </Container>
          </form>
        </Row>
      </Container>
    </div>
  );
};

export default Home;
