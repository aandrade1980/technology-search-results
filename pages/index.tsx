import type { NextPage } from 'next';
import Head from 'next/head';
import { useState } from 'react';

/** Components */
import { Container, Loading, Row } from '@nextui-org/react';
import { ResultList } from '../components/ResultList';
import { SearchForm } from '../components/SearchForm';

/** Types */
import { Result } from '../@types/sharedTypes';

const Home: NextPage = () => {
  const [searchText, setSearchText] = useState('');
  const [searchResults, setSearchResults] = useState<Result[]>([]);
  const [isSearching, setIsSearching] = useState(false);

  const handleSearch = async (e: React.FormEvent<HTMLFormElement>) => {
    e.preventDefault();

    if (!searchText) {
      return;
    }

    setIsSearching(true);

    const results = await fetch(`/api/getResults?searchText=${searchText}`);
    const data = await results.json();

    setSearchResults(data);
    setIsSearching(false);
  };

  console.log(searchResults);

  return (
    <div>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Container as="main">
        <Row justify="center" align="center">
          <h1>Welcome to Search Technology</h1>
        </Row>
        <Row justify="center" align="center">
          <SearchForm
            searchText={searchText}
            setSearchText={setSearchText}
            handleSearch={handleSearch}
            isSearching={isSearching}
          />
        </Row>

        {isSearching && (
          <Row justify="center" align="center" css={{ mt: 80 }}>
            <Loading size="xl" />
          </Row>
        )}

        {!isSearching && searchResults.length > 0 && (
          <ResultList searchResults={searchResults} />
        )}
      </Container>
    </div>
  );
};

export default Home;
